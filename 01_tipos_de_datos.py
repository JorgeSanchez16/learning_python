#*******************Consultar el tipo de dato*************
print(type("Hola Mundo")) # Tipo 'str' 
print(type(5)) # Tipo 'int' 
print(type(1.5)) # Tipo 'float'
print(type(True)) # Tipo 'bool'
print(type(1+2j)) # Tipo 'complex number'
print(type([1, 2, 3])) # Tipo 'lis'
print(type({'name':'Asabeneh'})) # Tipo 'Dict' Dictionary
print(type({1.5, 2.5, 3.6})) # Tipo 'set'
print(type((9.8, 3.14, 2.7))) #Tipo 'Tupla'
